package com.quanutrition.app.selectiondialogs;

public class SingleSelectionModel {
    private String id,label;

    public SingleSelectionModel(String id, String label) {
        this.id = id;
        this.label = label;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
